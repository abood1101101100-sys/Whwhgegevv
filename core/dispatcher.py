from __future__ import annotations

from typing import Any, Callable

COMMAND_HANDLERS: dict[str, Callable] = {}


def register(key: str) -> Callable:
    """
    مزخرف يسجّل الدالة في COMMAND_HANDLERS تحت المفتاح key.

    يدعم:
      - نص أمر دقيق:   @register("حظر")
      - بادئة (مسافة): @register("حظر ")   ← يتطابق مع أي نص يبدأ بـ "حظر "
      - تسمية مجموعة:  @register("moderation_commands")  ← للتوثيق فقط

    تكديس أكثر من @register على نفس الدالة مدعوم:
        @register("حظر ")
        @register("حظر")
        async def banHandler(c, m): ...
    """
    def decorator(fn: Callable) -> Callable:
        COMMAND_HANDLERS[key] = fn
        return fn
    return decorator


async def dispatch(text: str, c: Any, m: Any) -> bool:
    """
    يبحث عن معالج مناسب لنص الأمر المُرسَل ويستدعيه.

    منطق البحث (بالترتيب):
      1. مطابقة دقيقة:  text == key
      2. مطابقة بادئة:  key.endswith(" ") and text.startswith(key)

    Returns:
        True  — وُجد معالج وتمّ استدعاؤه.
        False — لا معالج مطابق (دع الـ group=28 handlers تعمل).
    """
    if text in COMMAND_HANDLERS:
        await COMMAND_HANDLERS[text](c, m)
        return True

    for cmd, handler in COMMAND_HANDLERS.items():
        if cmd.endswith(" ") and text.startswith(cmd):
            await handler(c, m)
            return True

    return False
